// 参数：服务名等、业务产生原因、金额
// 返回值：科目代码、科目名
// 作用：根据服务名查找是否具有改科目，并返回科目代码和科目名
export default (targetName, source, money) => {
  let temp = {code: '未配对', name: '未配对'}
  formJson.forEach((item) => {
    if (targetName.indexOf(item.keyword) !== -1) {
      switch (item.other) {
        case '':
            return temp = {
              code: item.code,
              name: item.name
            }
        case '业务产生原因为选项1':
            if (source === '员工福利') {
              return temp = {
                code: item.code,
                name: item.name
              }
            }
        case '业务产生原因为选项2':
            if (source === '业务招待') {
              return temp = {
                code: item.code,
                name: item.name
              }
            }
        case '业务产生原因为选项3':
            if (source === '员工差旅费用') {
              return temp = {
                code: item.code,
                name: item.name
              }
            }
        case '业务产生原因为选项4':
            if (source === '采购公司销售的商品') {
              return temp = {
                code: item.code,
                name: item.name
              }
            }
        case '金额>5000':
            if (Number(money) > 5000) {
              return temp = {
                code: item.code,
                name: item.name
              }
            }
        case '金额<5000':
            if (Number(money) < 5000) {
              return temp = {
                code: item.code,
                name: item.name
              }
            }
        default:
            return temp = {
              code: '未配对',
              name: '未配对'
            }
      }
    }
  })
  return temp
}
